    public static JsonObject getKehadiran(String urlString, String idakun, 
            String tgl_lahir, String tahun, String bulan, String usr_pass) 
            throws Exception {
        BufferedReader reader = null;
        String urlParamString;
        urlParamString = urlString;
        urlParamString += "?";
        urlParamString += "idakun=";
        urlParamString += URLEncoder.encode(idakun, "UTF-8");
        urlParamString += "&";
        urlParamString += "tgl_lahir=";
        urlParamString += URLEncoder.encode(tgl_lahir, "UTF-8");
        urlParamString += "&";
        urlParamString += "tahun=";
        urlParamString += URLEncoder.encode(tahun, "UTF-8");
        urlParamString += "&";
        urlParamString += "bulan=";
        urlParamString += URLEncoder.encode(bulan, "UTF-8");
        urlParamString += "&";
        urlParamString += "usr_pass=";
        urlParamString += URLEncoder.encode(usr_pass, "UTF-8");
        URL url;
        url = new URL(urlParamString);
        URLConnection connection = url.openConnection();
        StringBuilder response;
        try (BufferedReader in = new BufferedReader(
                new InputStreamReader(
                        connection.getInputStream()))) {
            response = new StringBuilder();
            String inputLine;
            while ((inputLine = in.readLine()) != null)
                response.append(inputLine);
        }        
        System.out.println(response.toString());
        GsonBuilder builder = new GsonBuilder();
        Gson gson = builder.create();
        JsonObject jsonObject;
        jsonObject = gson.fromJson(response.toString(), JsonObject.class);
        return jsonObject;
    }
