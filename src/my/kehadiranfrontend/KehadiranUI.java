    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tabel_model;
        tabel_model = (DefaultTableModel) tabel_output.getModel();
        tabel_model.setRowCount(0);
        String urlString = "http://localhost:18080/kehadiran";
        JsonObject readUrl;
        String param_id_akun;
        String param_tgl_lahir;
        String param_tahun;
        String param_bulan;
        String param_usr_password;
        JsonElement kehadiranStatus;
        JsonElement kehadiranMessage;
        JsonElement kehadiranData;
        param_id_akun = id_karyawan.getText();
        param_tgl_lahir = tgl_lahir.getText();
        param_tahun = tahun.getText();
        param_bulan = bulan.getText();
        param_usr_password = new String(usr_password.getPassword());
        try {        
            readUrl = getKehadiran(urlString, param_id_akun, param_tgl_lahir, param_tahun, param_bulan, param_usr_password);
            kehadiranStatus = readUrl.get("status");
            kehadiranMessage = readUrl.get("message");
            kehadiranData = readUrl.get("data");
            Boolean kehadiranDataisJsonObject = kehadiranData.isJsonObject();
            Boolean kehadiranDataisJsonArray = kehadiranData.isJsonArray();
            Boolean kehadiranDataisJsonPrimitive = kehadiranData.isJsonPrimitive();
            System.out.println("kehadiranDataisJsonObject: " + kehadiranDataisJsonObject);
            System.out.println("kehadiranDataisJsonArray: " + kehadiranDataisJsonArray);
            System.out.println("kehadiranDataisJsonPrimitive: " + kehadiranDataisJsonPrimitive);
            if (kehadiranDataisJsonPrimitive) {
                JOptionPane.showMessageDialog(null, "Status: " + kehadiranStatus.getAsString() + "\nPesan: " + kehadiranMessage.getAsString());
            } else if (kehadiranDataisJsonArray) {
                int jsonlen = kehadiranData.getAsJsonArray().size();
                for (int x = 0; x < jsonlen; x = x + 1) {
                    String id_record = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(0).getAsString();
                    String tanggal = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(1).getAsString();  
                    String id_akun = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(2).getAsString();              
                    String nama = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(3).getAsString();  
                    String id_divisi = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(4).getAsString();
                    String nama_div = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(5).getAsString();
                    String masuk = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(6).getAsString();
                    String pulang = readUrl.getAsJsonArray("data").get(x).getAsJsonArray().get(7).getAsString();
                    tabel_model.addRow(new Object[]{tanggal, nama, nama_div, masuk, pulang});
                }
                JOptionPane.showMessageDialog(null, "Status: " + kehadiranStatus.getAsString() + "\nPesan: " + kehadiranMessage.getAsString());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Status: error\nPesan: Gagal menghubungi server API. Hubungi Administrator. "); 
            Logger.getLogger(KehadiranUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitMouseClicked

